COMMENT *I/O PROCEDURES FOR THE SUDOKU SOLVER*

IF1
	INCLUDE C:\TASM\sudoku\sud_ct.mac
	INCLUDE C:\TASM\sudoku\sud_io.mac
ENDIF

IO_CODE SEGMENT PARA PUBLIC 'CODE'	
	ASSUME CS:IO_CODE
	
	PUBLIC	READ
	
	READ PROC FAR
		PUSH DX
		PUSH BX
		
		;Open the file
		FOPEN
		
		JNC READFILE
		MOV AX,EFOPEN
		JMP RETURN
		
		READFILE:
		MOV DX,BX
		MOV BX,AX
		FREAD MSIZE
		
		JNC SUCCESS
		MOV AX,EFREAD
		JMP CLOSEFILE
	
		SUCCESS:
		MOV AX,ESUCC
		
		CLOSEFILE:
		PUSH AX
		FCLOSE
		POP AX
		
		RETURN:
		POP BX
		POP DX
		RET
	READ ENDP
	
IO_CODE ENDS

END
