COMMENT *I/O PROCEDURES FOR THE SUDOKU SOLVER*

IF1
	INCLUDE C:\TASM\sudoku\sud_ct.mac
	INCLUDE C:\TASM\sudoku\sud_io.mac
ENDIF

IO_CODE SEGMENT PARA PUBLIC 'CODE'	
	ASSUME CS:IO_CODE
	
	PUBLIC	READ
	
	READ PROC FAR
		PUSH AX
		;Open the file
		FOPEN
		
		JNC READFILE
		MOV DX,EFOPEN
		JMP RETURN
		
		READFILE:
		MOV DX,BX
		MOV BX,AX
		FREAD MSIZE
		
		JNC SUCCESS
		MOV DX,EFREAD
		JMP CLOSEFILE
	
		SUCCESS:
		MOV DX,0
		
		CLOSEFILE:
		FCLOSE
		
		RETURN:
		POP AX
		RET
	READ ENDP
	
IO_CODE ENDS

END
